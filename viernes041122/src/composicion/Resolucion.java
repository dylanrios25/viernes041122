package composicion;

public class Resolucion {
   private int anchura;
   private int altura;
   
   public Resolucion(int anchura,int altura) {
	   this.anchura=anchura;
	   this.altura=altura;
   }

public int getAnchura() {
	return anchura;
}

public void setAnchura(int anchura) {
	this.anchura = anchura;
}

public int getAltura() {
	return altura;
}

public void setAltura(int altura) {
	this.altura = altura;
}


}
